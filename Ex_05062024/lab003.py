# Print command
# different arguments in the print command
print("hello", "world", 123, 3.14, True)
# seperator by default has single space
print("hello", "world", 123, 3.14, True, sep="")
print("hello", "world", 123, 3.14, True, sep="   ")
print("hello", "world", 123, 3.14, True, sep="_")
print("hello", "world", 123, 3.14, True, sep="---")
# 1 argument
print("Hi, My name is Shruti")
# 2 args
print("Hi, My name is Shruti", "Belsare", sep="-")


